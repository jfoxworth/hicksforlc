                                <h2 class="font-secondary topmargin">My Story</h2>

<span>Born and raised in Elizabethtown, KY, after graduating high school, I was granted a full scholarship to University of Louisville, however the country called me to serve instead. I spent the next 2 years in North Carolina and 4 years overseas where my fighter unit deployed the first strike in the Iraq War effort. Spent a year at Niagara Falls Air Station and finished my last 6 years at Ellington Field as a Guardsman. After about 13 years of service I left the Air Force and our family settled in League City. Starting out, married my wife, we bought our first home in the older part of League City and had four beautiful daughters. Along the way, with the family backing me, we chose to chase a career opportunity as an Engineer and Project Manager and relocated to Everett, Washington. For the next few years; high taxes, unrealistic real estate prices, death of a family member, and missing our true home sent us back to our favorite town, League City.
After Military life, I found my way in Maritime, Shipping and Oil & Gas, where I have successfully served as an Engineer, Surveyor, Classroom Instructor, Trainer, Project Manager and Program Manager. While working for 4 global companies in my field, I have made large impacts setting the tone as a model of efficiency. While still being the best option to ship our goods; Maritime is the most wasteful and over-regulated industry in the world. It is quite a challenge on a daily basis, but the challenges motivate me, I perform best under pressure, it has kept me in the industry.</span>

                                <h2 class="font-secondary topmargin">Accomplishments</h2>

<span>I have successfully engineered, managed and delivered millions of dollars in projects all around the globe, under tight timelines, and even tighter budgets. My customers included several Oil and Gas majors, US and foreign governments, merchant mariner academies, cruise liners, port authorities, local colleges and high school vocational programs.
In my current position, I manage a program and provide as a subject matter expert for an investment startup inside of a multinational corporation, where the vision is to use digitization and data in the Maritime industry to eliminate corruption, waste and provide transparency. In the program, I found a way during the first 6 months on the job, to reduce an extremely out of control budget by less than half, while delivering our customers the same product, and ramping the operational efficiency by at least more than double.</span>

                                <h2 class="font-secondary topmargin">My Vision</h2>

<span><p>I want to put my global experience and skills to work in our city. Most Government is inefficient, bloated, and wasteful. Small, effective government that works for the taxpayers and does not abuse their trust or hard earned money is the way to go. My experiences, along with my high energy and passion for our town and country has aligned me well to serve League City. I have real world experience and real world success. I know how to grind and run a budget, professional and personal, and fully appreciate that money doesn't grow on trees. I believe in common sense governing, be a good steward of the taxpayer money, and uphold The Constitution as written. My professional ideology is performance over attendance, results over process, accountability and transparency.</p>

<p>My promises to League City: Tax increases should always be a last resort, there are almost always other avenues. I will never knowingly choose to hire a corrupt or crooked contractor, I will never, ever vote for any measure that contradicts your rights. I don’t like the idea of government officials voting for their own raises. No government official deserves a raise right now! I will call out and shame any corruption or unethical practice.
